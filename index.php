    <h1 class="display-4">Communications antennas</h1>
    <p class="lead">Hubble performs in response to detailed instructions from people on the ground. The antennas allow technicians to communicate with the telescope, telling it what to do and when to do it. Four antennas receive and send information to a set of satellites, which in turn communicate with Earth.</p>
    <p class="lead">        </p>
    <h1 class="display-4">Spacecraft systems</h1>
    <p class="lead">Hubble is operated by commands from the ground. Several spacecraft systems are in place to keep Hubble functioning smoothly.</p>
    <p class="lead">        </p>
    <h1 class="display-4">Solar arrays</h1>
    <p class="lead">Hubble is powered by sunlight. Each wing-like array has solar cells that convert the Sun’s energy into electricity. Some of that electricity runs the telescope, some is stored in onboard batteries for the periods when Hubble is in Earth’s shadow.</p>
    <p class="lead">        </p>
    <h1 class="display-4">Computers and automation</h1>
    <p class="lead">Several computers and microprocessors reside in Hubble’s body and in each science instrument. There are two main computers. One talks to the instruments, sends commands and other information, and transmits data; the other handles pointing control, gyroscopes and other system-wide functions.</p>
    <p class="lead">        </p>
    <h1 class="display-4">Thermal protection</h1>
    <p class="lead">Hubble has blanket of multilayered insulation, which protects the telescope from temperature extremes</p>
    <p class="lead">        </p>
    <h1 class="display-4">Pointing system</h1>
    <p class="lead">Hubble uses a combination of gyroscopes, reaction wheels and Fine Guidance Sensors to orient itself.</p>
    <p class="lead">        </p>
    <h1 class="display-4">Science of Hubble</h1>
    <p class="lead">Hubble is one of NASA’s most successful and long-lasting science missions. It has beamed hundreds of thousands of images back to Earth, shedding light on many of the great mysteries of astronomy.

Among its many discoveries, Hubble has revealed the age of the universe to be about 13.8 billion years, much more accurate than the old range of anywhere from 10 to 20 billion years. Hubble played a key role in the discovery of dark energy, a mysterious force that causes the expansion of the universe to accelerate.

Hubble has shown scientists galaxies in all stages of evolution, including galaxies that were around when the universe was still young, helping them understand how galaxies form. It found protoplanetary disks, clumps of gas and dust around young stars that likely function as birthing grounds for new planets. It discovered that gamma-ray bursts — strange, incredibly powerful explosions of energy — can occur in far-distant galaxies when massive stars collapse. And these are only a handful of its many continuing contributions to astronomy.</p>
  
