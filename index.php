    <h1 class="display-3"></h1>
    <p class="lead">Hubble’s 1,825 pound, 7.8-foot (2.4-meter) diameter primary mirror collects light from its astronomical target and reflex it to a 12-inch (0.3-meter) diameter secondary mirror located in the optical tube. This secondary mirror then reflects the light through a hole in the primary mirror to form an image at the telescope’s focal plane. There it is intercepted by pick-off mirrors that pass it into the scientific instruments. Hubble’s mirrors are made of ultra-low expansion glass kept at a “room temperature” of about 70°F (21°C) to avoid warping. The reflecting services are coated with a 3/1000,000-inch layer of pure aluminum and protected by a 1/1000,000-inch layer of magnesium fluoride that also makes the mirror is more reflective to ultraviolet light.</p>
